package model;

public class Aula {

	public String id;
	public String idDisciplina;
	public String idProfessor;

	public Aula() {
		
		super();
	}

	public Aula(String id, String idDisciplina, String idProfessor) {
		
		super();
		this.id = id;
		this.idDisciplina = idDisciplina;
		this.idProfessor = idProfessor;
	}

}
